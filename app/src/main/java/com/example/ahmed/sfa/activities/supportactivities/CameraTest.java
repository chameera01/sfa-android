    //after calling the capture method of ImageManager
    //it will send the activity to our class directly
    //we have to implements this method in order to manage the result
    @Override
    protected void onActivityResult(int requestCode,int resultCode,Intent data) {

        if (requestCode == ImageManager.CAMERA_REQUEST) {
            Toast.makeText(getApplicationContext(),"Captured",Toast.LENGTH_SHORT).show();
            if(resultCode==Activity.RESULT_OK) {
                Bitmap photo = (Bitmap)data.getExtras().get("data");
                im.saveImage(photo,"CUS_IMG_"+currentPhotoNum);
                //imgView.setImageBitmap(photo);
                imgView.setImageBitmap(im.retrieveImage("CUS_IMG_"+currentPhotoNum));
                currentPhotoNum++;
                //Toast.makeText(getApplicationContext(),"Captured",Toast.LENGTH_SHORT).show();
            }
        } else{
            super.onActivityResult(requestCode, resultCode, data);
        }
    }
